// 20. forEach() method


// forEach method in js
// purpose : Executes a function for each element 
// return value : Undefined
// Chainable :no
// Changes Original array : No
// Common Use : Logging,savingd,Data

// Ex: forEach just : just loops - doesn't return anything



let num = [1,2,3,4,5];
num.forEach((ele)=>{
    console.log(ele*2);
})
console.log(num);



let names = ["kaif","saboor","rayyan","kamran"]

names.forEach((x)=> {
    console.log(x);
})


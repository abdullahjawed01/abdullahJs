// Assignment Operator


// | Operator     Examples       Same As
// | -------- |   ----------     ----------
// | '='      |   'x = 5'      | 'x =  5'       |
// | '+='     |   'x += 5 '    | 'x = x + 5'    |
// | '-='     |   'x -= 5'     | 'x = x - 5'    |
// | '*='     |   'x *= 5'     | 'x = x * 5'    |
// | '/='     |   'x /= 5'     | 'x = x / 5'    |
// | '%='     |   'x %= 5'     | 'x = x % 5'    |
// | '**='    |   'x **53'     | 'x = x ** 5'   |

let box = 10;
console.log(box += 5);
console.log(box -= 5);
console.log(box *= 5);
console.log(box /= 5);
console.log(box %= 5);
console.log(box **= 5);
